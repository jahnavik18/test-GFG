class Calculator{
    int a;
    int b;
    int c;
    double x;
    double y;
    
    public Calculator(){
    }
    public int sum(int a,int b){
        return a+b;
    }
    public int sum(int a,int b,int c){
        return a+b+c;
    }
    public double sum(double x,double y){
        return x+y;
    }
}
