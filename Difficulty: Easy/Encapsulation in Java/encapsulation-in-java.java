class Person{
    private String name;
    private int age;
    Person(){
            this.name="Geeks";
            this.age=10;
    }
    public String getName(){
        return this.name;
    }
    public void setName(String name){
        this.name=name;
    }
    public int getAge(){
        return this.age;
    }
    public void setAge(int age){
        this.age=age;
    }
}